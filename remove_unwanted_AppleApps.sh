#!/bin/zsh

# Deletes unwanted bundled Apple apps on your Mac
echo  Deletes unwanted bundled Apple apps on your Mac


sudo rm -rf /Applications/iMovie.app
say iMovie removed
sudo rm -rf /Applications/GarageBand.app
say GarageBand removed
sudo rm -rf /Applications/Pages.app
say Pages removed!
sudo rm -rf /Applications/Keynote.app
say say goodbye to keynote!
sudo rm -rf /Applications/Numbers.app
say there goes the numbers!
say All done! all apps removed , exiting

exit
